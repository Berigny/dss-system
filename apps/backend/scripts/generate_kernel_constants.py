class ImmutableKernelError(RuntimeError):
    """Raised when runtime code attempts to mutate a frozen kernel constant."""

    code = "IMMUTABLE_KERNEL"


class _ImmutableMapping(Mapping):
    """Recursive, runtime-immutable mapping view used to freeze kernel registries."""

    __slots__ = ("_data",)

    def __init__(self, data: Mapping[Any, Any]) -> None:
        self._data = {{k: _freeze(v) for k, v in data.items()}}

    def __getitem__(self, key: Any) -> Any:
        return self._data[key]

    def __iter__(self):
        return iter(self._data)

    def __len__(self) -> int:
        return len(self._data)

    def __repr__(self) -> str:
        return f"_ImmutableMapping({{self._data!r}})"

    def __setitem__(self, key: Any, value: Any) -> None:
        raise ImmutableKernelError(
            f"IMMUTABLE_KERNEL: cannot modify {{key!r}} in frozen kernel mapping"
        )

    def __delitem__(self, key: Any) -> None:
        raise ImmutableKernelError(
            f"IMMUTABLE_KERNEL: cannot delete {{key!r}} from frozen kernel mapping"
        )

    def update(self, *args, **kwargs) -> None:
        raise ImmutableKernelError("IMMUTABLE_KERNEL: cannot update frozen kernel mapping")

    def pop(self, key: Any, *args) -> Any:
        raise ImmutableKernelError(
            f"IMMUTABLE_KERNEL: cannot pop {{key!r}} from frozen kernel mapping"
        )

    def popitem(self) -> tuple[Any, Any]:
        raise ImmutableKernelError("IMMUTABLE_KERNEL: cannot pop item from frozen kernel mapping")

    def clear(self) -> None:
        raise ImmutableKernelError("IMMUTABLE_KERNEL: cannot clear frozen kernel mapping")

    def setdefault(self, key: Any, default: Any = None) -> Any:
        raise ImmutableKernelError(
            f"IMMUTABLE_KERNEL: cannot setdefault {{key!r}} in frozen kernel mapping"
        )


def _freeze(value: Any) -> Any:
    """Recursively freeze mutable kernel values at import time."""
    if isinstance(value, dict):
        return _ImmutableMapping(value)
    if isinstance(value, list):
        return tuple(_freeze(v) for v in value)
    return value


