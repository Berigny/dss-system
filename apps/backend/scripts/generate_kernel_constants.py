def _load_ksr(repo_root: Path) -> dict[str, Any]:
    yaml_path = repo_root / "backend" / "kernel" / "semantic_registry.yaml"
    if yaml_path.exists():
        return yaml.safe_load(yaml_path.read_text())

    password = os.getenv("DSS_KSR_PASSWORD")
    if not password:
        raise RuntimeError(
            "No plaintext KSR found and DSS_KSR_PASSWORD is not set. "
            "Cannot generate constants from encrypted envelope."
        )
    return load_ksr_yaml(password)


def _indent(lines: str, width: int = 4) -> str:
    pad = " " * width
    return "\n".join(pad + line for line in lines.splitlines())


def _dict_repr(mapping: dict[Any, Any]) -> str:
    items = ",\n".join(f"    {k!r}: {v!r}" for k, v in mapping.items())
    return "{\n" + items + ",\n}"


def _enum_mapping_repr(mapping: dict[str, str]) -> str:
    """Render a mapping whose values are enum member references (unquoted)."""
    items = ",\n".join(f"    {k!r}: {v}" for k, v in mapping.items())
    return "{\n" + items + ",\n}"


